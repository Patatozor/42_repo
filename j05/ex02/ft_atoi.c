int	ft_atoi(char *str)
{
	int neg;
	int i;
	int nb;

	i = 0;
	neg = 1;
	nb = 0;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb *= 10;
		nb += str[i] - '0';
		i++;
	}
	return (neg * nb);
}
