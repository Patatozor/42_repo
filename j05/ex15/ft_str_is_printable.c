int	ft_str_is_printable(char *str)
{
	if (*str == '\0')
		return (1);
	else
	{
		while (*str != '\0')
		{
			if (*str >= ' ' && *str <= 127)
				str++;
			else
				return (0);
		}
		return (1);
	}
}
